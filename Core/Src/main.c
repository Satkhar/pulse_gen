  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */

  mode = 0; // init mode. pulse
  uint32_t i = 0;

  // var for pulse

  uint32_t pulse_count = 0;

  // var for trianlge
  uint8_t test_triangle = 0;
  uint32_t triangle_cur_time = 0;
  uint32_t triangle_up_time = 0;

  // var for trapeze and
  uint32_t trapeze_up_count = 0;

